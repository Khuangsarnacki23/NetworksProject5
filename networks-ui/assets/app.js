function wasmView(spec) {
  const state = {};
  const view = h("section", { class: "view", id: `view-${spec.id}` });
  const term = h("div", { class: "term" }, h("span", { class: "meta" }, "Pick a mode and a file, then hit Run — output shows up here."));
  const cmdEl = h("div", { class: "cmd" });

  const updateCmd = () => { cmdEl.textContent = `./${spec.binary} ${spec.buildArgs(state).display}`; };

  // "Peek inside" runs the program's own print mode on the file
  async function peek(ref, fileSpec) {
    term.textContent = "peeking…";
    try {
      const bytes = await bytesFor(spec.project, ref);
      const argv = fileSpec.peekArgs(ref.name);
      const res = await runWasm(spec.binary, argv, { [ref.name]: bytes });
      const lines = res.out.split("\n");
      const shown = lines.slice(0, 15).join("\n");
      term.textContent = "";
      term.append(h("span", { class: "meta" }, `contents of ${ref.name} (via ${fileSpec.peekLabel}):\n`));
      term.append(shown || "(no printable records)");
      if (lines.length > 16) term.append(h("span", { class: "meta" }, `\n… ${lines.length - 16} more lines — run it to see everything`));
    } catch (e) {
      term.textContent = ""; term.append(h("span", { class: "err" }, `could not peek: ${e.message}`));
    }
  }

  const fileFields = spec.files.map(f =>
    fileSelect(`${spec.id}-${f.key}`, f.label, f.samples, state, f.key, spec.project,
      f.peekArgs ? { onPeek: ref => peek(ref, f) } : null, updateCmd));

  const runBtn = h("button", { class: "runbtn", onclick: run }, "Run ▸");
  async function run() {
    runBtn.disabled = true;
    term.textContent = "running…";
    try {
      const { argv, files } = await spec.resolve(state);
      const res = await runWasm(spec.binary, argv, files);
      termWrite(term, res, `./${spec.binary} ${spec.buildArgs(state).display}`);
    } catch (e) {
      term.textContent = ""; term.append(h("span", { class: "err" }, `error: ${e.message}`));
    }
    runBtn.disabled = false;
  }

  view.append(
    crumb(spec.shortTitle),
    h("div", { class: "pagehead" },
      h("h2", {}, spec.title, h("span", { class: "badge wasm" }, "runs right in your browser")),
      h("p", { class: "tagline" }, spec.blurb)),
    h("div", { class: "card" },
      h("h3", {}, "1 · Choose a mode"),
      modeButtons(spec.modes, state, updateCmd)),
    h("div", { class: "card" },
      h("h3", {}, "2 · Pick your input " + (spec.files.length > 1 ? "files" : "file")),
      h("div", { class: "row" }, fileFields),
      h("p", { class: "note" }, "Not sure what a file holds? They're binary, so a text editor shows gibberish — use ", h("b", {}, "Peek inside"), " for a decoded preview, or read the blurb under each picker.")),
    h("div", { class: "card" },
      h("h3", {}, "3 · Run it"),
      h("div", { class: "cmdbar" }, cmdEl, runBtn),
      h("p", { class: "note" }, "That's the exact command you'd type in a terminal — this page just runs it for you.")),
    h("div", { class: "card" }, h("h3", {}, "Output"), term),
  );
  updateCmd();
  return view;
}

