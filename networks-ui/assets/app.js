function proj5View() {
  const view = h("section", { class: "view", id: "view-proj5" });
  const term = h("div", { class: "term" }, h("span", { class: "meta" }, "Point me at a running socketsd server and send a command — the reply comes back over a real TCP connection."));
  const cmdEl = h("div", { class: "cmd" });
  const state = { mode: "-t" };

  const host = h("input", { type: "text", placeholder: "e.g. my-socketsd.fly.dev", value: localStorage.getItem("p5host") || "" });
  const port = h("input", { type: "number", placeholder: "4642", value: localStorage.getItem("p5port") || "4642" });

  const FIELDS = {
    "-t": [["n", "Team name (-n)"]],
    "-b": [["n", "Team name (-n)"], ["u", "Player name (-u)"]],
    "-g": [["d", "Date (-d)"], ["o", "Time (-o)"], ["C", "Location (-C)"], ["H", "Home team (-H)"], ["A", "Away team (-A)"]],
    "-r": [["G", "Game ID (-G)"], ["n", "Team (-n)"], ["u", "Player (-u)"], ["P", "Points (-P)"], ["S", "Assists (-S)"], ["R", "Rebounds (-R)"], ["M", "Minutes (-M)"]],
    "-l": [["u", "Player (-u), or leave blank"], ["n", "Team (-n), or leave blank"], ["G", "Game ID (-G), or leave blank"]],
    "-j": [],
  };
  const MODES = [
    { flag: "-t", label: "Add a team" }, { flag: "-b", label: "Add a player" }, { flag: "-g", label: "Create a game" },
    { flag: "-r", label: "Record stats" }, { flag: "-l", label: "Look up stats" }, { flag: "-j", label: "Dump JSON" },
  ];

  const fieldWrap = h("div", { class: "row" });
  const inputs = {};

  function renderFields() {
    fieldWrap.textContent = "";
    for (const k of Object.keys(inputs)) delete inputs[k];
    for (const [flag, label] of FIELDS[state.mode]) {
      const inp = h("input", { type: "text", oninput: updateCmd });
      inputs[flag] = inp;
      fieldWrap.append(h("div", { class: "field" }, h("label", {}, label), inp));
    }
    if (!FIELDS[state.mode].length) fieldWrap.append(h("p", { class: "note" }, "No extra arguments needed — the server writes everything it knows to league_dump.json."));
  }

  function currentArgs() {
    const args = { host: host.value.trim(), port: port.value.trim(), mode: state.mode, fields: {} };
    for (const [flag, inp] of Object.entries(inputs)) {
      const v = inp.value.trim();
      if (v) args.fields[flag] = v;
    }
    return args;
  }

  function updateCmd() {
    const a = currentArgs();
    let c = `./sockets -h ${a.host || "<host>"} -p ${a.port || "<port>"} ${a.mode}`;
    for (const [f, v] of Object.entries(a.fields)) c += ` -${f} ${/\s/.test(v) ? `'${v}'` : v}`;
    cmdEl.textContent = c;
  }

  const modes = modeButtons(MODES, state, () => { renderFields(); updateCmd(); });
  const runBtn = h("button", { class: "runbtn", onclick: run }, "Send ▸");

  async function run() {
    const a = currentArgs();
    if (!a.host || !a.port) { term.textContent = ""; term.append(h("span", { class: "err" }, "Fill in the socketsd server host and port first (see the note below for hosting it).")); return; }
    localStorage.setItem("p5host", a.host); localStorage.setItem("p5port", a.port);
    runBtn.disabled = true;
    term.textContent = "connecting over TCP…";
    try {
      const r = await fetch("/api/sockets", {
        method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify(a),
      });
      const j = await r.json();
      if (j.error) { term.textContent = ""; term.append(h("span", { class: "err" }, `error: ${j.error}`)); }
      else termWrite(term, { out: j.stdout, err: j.stderr, code: j.exitCode }, j.command);
    } catch (e) {
      term.textContent = ""; term.append(h("span", { class: "err" }, `request failed: ${e.message}`));
    }
    runBtn.disabled = false;
  }

  host.addEventListener("input", updateCmd); port.addEventListener("input", updateCmd);

  view.append(
    crumb("Project 5"),
    h("div", { class: "pagehead" },
      h("h2", {}, "Project 5 · League Stats over TCP", h("span", { class: "badge tcp" }, "real sockets, real server")),
      h("p", { class: "tagline" }, "The genuine article: the compiled C client opens an actual TCP connection to a live socketsd server and speaks the league protocol — register teams and players, schedule games, record box scores, query stats.")),
    h("div", { class: "card" },
      h("h3", {}, "1 · Where is your socketsd server?"),
      h("div", { class: "row" },
        h("div", { class: "field" }, h("label", {}, "Host (-h)"), host),
        h("div", { class: "field" }, h("label", {}, "Port (-p)"), port)),
      h("p", { class: "note" }, "Don't have one running? The ", h("code", {}, "server/"), " folder has a Dockerfile — deploy it to Fly.io or any box with a public port (instructions in the README).")),
    h("div", { class: "card" },
      h("h3", {}, "2 · Build your command"),
      modes, h("div", { style: "height:14px" }), fieldWrap,
      h("div", { class: "cmdbar" }, cmdEl, runBtn)),
    h("div", { class: "card" }, h("h3", {}, "Reply from the server"), term),
    h("p", { class: "note" }, "Order matters, like in real life: register a team before its players, create a game before recording stats. Look-up accepts exactly one of player / team / game ID."),
  );
  renderFields(); updateCmd();
  return view;
}

